const isFxTypeWithEmptyBlocks = (item) => {
  if (!item.value) return true;
  return (
    item.value?.type === "fx" &&
    Array.isArray(item.value.blocks) &&
    item.value.blocks.length === 0
  );
};

const checkBlocksArrayIsEmpty = (data) => {
  return data.some(isFxTypeWithEmptyBlocks);
};

const isDataEmpty = (data) => {
  return data.some((item) => {
    const hasKey = !!item.key;
    const isFxEmpty = isFxTypeWithEmptyBlocks(item);
    return !hasKey && !isFxEmpty; // invalid case
  });
};

export const checkEmptyFields = (data, type) => {
  if (type === HEADER) {
    if (isDataEmpty(data)) {
      return HTTP_ERRORS.EMPTY_HEADERS_FIELDS;
    } else {
      return null;
    }
  }
  if (type === PARAMS) {
    if (isDataEmpty(data)) {
      return HTTP_ERRORS.EMPTY_PARAMS_FIELDS;
    } else {
      return null;
    }
  }
};

