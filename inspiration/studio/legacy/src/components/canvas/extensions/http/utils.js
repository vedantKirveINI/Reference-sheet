export const isCurlCommand = (input) => /curl\s+/.test(input);

export const transformObjectToRows = (data) => {
  return Object.entries(data).map(([key, value]) => ({
    expand: false,
    rowid: _.uniqueId(Date.now()),
    key: key,
    value: createPrimitive(value),
    valueStr: value,
  }));
};

