export const createPrimitive = (value) => {
  return {
    type: "fx",
    blocks: [
      {
        type: "PRIMITIVES",
        value,
      },
    ],
  };
};

const updateRequestBody = (bodyData, bodyInfo, httpData) => {
  httpData.body_type = bodyInfo.body_type;

  if (!_.isEmpty(bodyData)) {
    switch (bodyInfo.body_type) {
      case RequestBodyType.RAW:
        httpData.subType =
          subTypeOptions.find(
            (option) => option.id === bodyInfo.body_sub_type
          ) || subTypeOptions[0];

        if (httpData.subType.id !== RequestRawDataType.JSON.id) {
          httpData.rawData = createPrimitive(JSON.stringify(bodyData));
        } else {
          httpData.rawData = convertJsonToGridSchema({ "": { ...bodyData } });
        }
        break;
      case RequestBodyType.FORM_DATA:
        httpData.formGridData = transformObjectToRows(bodyData);
        break;
      case RequestBodyType.X_WWW_FORM_URL_ENCODED: {
        /* Not sure about the reason behind this logic. But it seems incorrect and the right implementation is handled below. */
        /* 
          const urlEncodedData = queryStringToObject(bodyData); 
          httpData.urlencodedGridData = transformObjectToRows(urlEncodedData);
         */
        httpData.urlencodedGridData = transformObjectToRows(bodyData);
        break;
      }
      default:
        break;
    }
  }
};

export const queryStringToObject = (queryString) => {
  queryString.split("&").reduce((acc, pair) => {
    const [key, value] = pair.split("=");
    acc[decodeURIComponent(key)] = decodeURIComponent(value);
    return acc;
  }, {});
};

export const parsedCurlJSONToHttpData = (parsedCurlJSON) => {
  if (_.isEmpty(parsedCurlJSON)) return;
  const httpData = {};
  for (const [key, value] of Object.entries(parsedCurlJSON)) {
    switch (key) {
      case "headers":
        httpData.headers = transformObjectToRows(value);
        break;
      case "method":
        httpData.method =
          methodOptions.find((option) => option === value) || methodOptions[0];
        break;
      case "body_info": {
        let body_type = value.type;
        let body_sub_type = value.sub_type;
        let body = parsedCurlJSON.body;
        /* Not sure about the reason behind this logic. Commenting it as it seems unnecessary */
        // if (
        //   parsedCurlJSON?.headers?.["Content-Type"] ===
        //   RequestBodyType.X_WWW_FORM_URL_ENCODED
        // ) {
        //   body_type = RequestBodyType.X_WWW_FORM_URL_ENCODED;
        // }
        updateRequestBody(body, { body_type, body_sub_type }, httpData);
        break;
      }
      case "url": {
        const urlFromCurl = createPrimitive(value);
        httpData.url = { ...urlFromCurl, text: value };
        break;
      }
      case "query_params":
        httpData.query_params = transformObjectToRows(value);
        break;
      default:
        break;
    }
  }
  return httpData;
};

